<div class="box">
  <h1>TIP</h1><div class="line"></div>
  <p>In Proto-Semitic, the feminine suffix was always <b>-at</b>, but in Arabic and Hebrew the <b>[t]</b> became silent in the final position, producing <b>-a(t)</b>. However, it was not lost in the feminine terms <b>بنت (bint, “daughter”)</b> and <b>أخت (ʔuḵt, “sister”)</b> — analogous to Standard Arabic <b>ابن (ibn, "son")</b> and <b>أخ (ʔaḵ, "brother")</b> with the original feminine ending. It’s conserved as well in the <b>3F</b> conjugation of the Past Tense (<b>-at</b>); in that sense, the feminine conjugation is analogous to <b>3M (فعل “faʕal”)</b> with an un-silenced <b>ta marbūṭa</b> <b>(فعلة “faʕalat”)</b>.</p>
</div>

<h1>Gender Agreement</h1><div class="line"></div>

<p>But what does the <b>ta marbūṭa</b> represent? Primarily, it indicates female grammatical gender. Generally, terms without <b>ta marbūṭa</b> (e.g. <a href="https://www.wiktionary.org/wiki/صندوق#South_Levantine_Arabic" target="_blank">صندوق</a>) are grammatically male, while those that have it (e.g. <a href="https://www.wiktionary.org/wiki/علبة#South_Levantine_Arabic" target="_blank">علبة</a>) are grammatically female. In the case of nouns, the <b>ta marbūṭa</b> is part of the word itself. However, recall that Arabic requires <b>gender agreement</b>; in the case of adjectives, the <b>ta marbūṭa</b> must attached to an adjective that modifies a grammatically feminine noun.</p>

<div class="flex">
  <div class="content">
    <div class="arb">صندوق كبير</div>
    <div class="eng">a big box</div>
    <div class="gloss"><span class="blu">M</span>.box <span class="blu">M</span>.big</div>
  </div>
  <div class="content">
    <div class="arb">علب<span class="mag">ة</span> كبير<span class="mag">ة</span></div>
    <div class="eng">a big tin</div>
    <div class="gloss"><span class="mag">F</span>.box <span class="mag">F</span>.big</div>
  </div>
</div>

<p>Of course, <b>definiteness agreement</b> continues to apply:</p>

