<h1>the Construct Form</h1><div class="line"></div>

<p>While the meaning and function of the <b>ta marbūṭa</b> is not too complicated, it may be confusing to understand when the hidden <b>/t/</b> is silent or not. As mentioned earlier, the <b>ta marbūṭa</b> isn't pronounced unless there's something attached to it. When there's a suffix attached to <b>ta marbūṭa</b>, it even changes to an ordinary <b>ta</b>.</p>

<div class="flex flashcard">
  <div class="content">
    <div class="front">
      <div class="arb">مَدرَس<span class="mag">ة</span></div>
      <div class="eng">madras<span class="mag">e</span></div>
    </div>
    <div class="back">
      <div class="arb">مَدرَس<span class="mag">ت</span>ي</div>
      <div class="eng">madras<span class="mag">t</span>-i</div>
    </div>
  </div>
</div>

<div class="flex flashcard">
  <div class="content">
    <div class="front">
      <div class="arb">سَيّار<span class="mag">ة</span></div>
      <div class="eng">sayyār<span class="mag">a</span></div>
    </div>
    <div class="back">
      <div class="arb">سَيّار<span class="mag">ت</span>ها</div>
      <div class="eng">sayyār<span class="mag">it</span>-ha</div>
    </div>
  </div>
</div>

<p>However, that's not the only way that something can be "attached" to the <b>ta marbūṭa</b>. lf we change these possessive suffixes to actual nouns, the <b>ta marbūṭa</b> is still pronounced.</p>

<div class="flex">
  <div class="num">2A</div>
  <div class="content">
    <div class="arb">سَيّار<span class="mag">ت</span>ها</div>
    <div class="eng">sayyār<span class="mag">it</span>-ha</div>
  </div>
  <div class="num">2B</div>
  <div class="content">
    <div class="arb">سَيّار<span class="mag">ة</span> أختي</div>
    <div class="eng">sayyār<span class="mag">it</span> ʔuxti</div>
  </div>
</div>

<p>Another way to look at it is that <b>2B</b> is an <b>iḍāfa</b> construction, which is used to show possession or connection (cf. “of” in English) and turns the whole phrase into a single unit. Any time the <b>ta marbūṭa</b> is in the middle of this construction, it's pronounced as <b>/t/</b>.</p>

<div class="flex">
  <div class="content">
    <div class="arb">لهج<span class="mag">ة</span> فلسطين</div>
    <div class="eng">lahj<span class="mag">it</span> falasṭīn</div>
  </div>
</div>

<p>In all other situations — as in the case of adjectives — the <b>ta marbūṭa</b> is pronounced as a vowel, since what comes after is not grammatically attached to it in any way.</p>

<div class="flex">
  <div class="content">
    <div class="arb">اللهج<span class="mag">ة</span> الفلسطين<span class="mag">ة</span></div>
    <div class="eng">il-lahj<span class="mag">e</span> il-falasṭīniyy<span class="mag">e</span></div>
  </div>
</div>

<p>And that's everything you need to know about the <b>ta marbūṭa</b>!</p>

