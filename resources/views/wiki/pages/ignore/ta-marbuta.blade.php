<h1>Other Meanings</h1><div class="line"></div>

<p>However, the <b>ta marbūṭa</b> has other functions as well. Aside from marking gender, the <b>ta marbūṭa</b> creates a single countable noun out of an abstract concept or a collective plural. For instance, <b>ليل (lēl)</b> means "night-time", while a single "night" is <b>ليلة (lēle)</b> — with <b>ta marbūṭa</b>.</p>

