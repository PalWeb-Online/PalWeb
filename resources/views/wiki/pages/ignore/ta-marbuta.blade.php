<h1>The Ta Marbūṭa</h1><div class="line"></div>

<p>In this lesson, we’re going to discuss the <b>ta marbūṭa</b>.</p>

<p>First of all, the <b>ta marbūṭa (ة)</b> is technically not one of the letters in the Arabic abjad, because it's really a variation on the letter <b>ت (ta)</b>. In fact, <b>ta marbūṭa</b> means “tied ta” because its shape is like a <b>ta</b> wrapped into a circle. It only comes at the end of a word, and its pronunciation is either <b>/e/</b> or <b>/a/</b> depending on the speaker’s dialect (see <b>Lesson 6</b> for more on how this vowel is pronounced in different situations). However, the reason it's written as a variation of the letter <b>ta</b> is because when something is attached after it, it becomes “untied” and turns into a regular <b>ta</b>.</p>

