<p>Interestingly, the <b>ta marbūṭa</b> can turn an adjective into an abstract noun as well; this is especially common among philosophical or political terms, somewhat similar to the alternation of <b>“-ist”</b> and <b>“-ism”</b> suffixes in English.</p>

