<h1>Regular Adjectives</h1><div class="line"></div>

<p>In this section, we're going to discuss adjectives. The use of adjectives in Arabic is quite simple, and I basically explained it the section on <a href="?page=02">the Definite Article</a>. Just to review, there are three things you need to know to attach an adjective to a noun:</p>

<h3><img src="img/check.svg" width="36px">
  Adjectives follow the noun they modify.</h3>

<div class="flex">
  <div class="num">1A</div>
  <div class="content">
    <div class="arb">بيت كبير</div>
    <div class="eng">a big house</div>
    <div class="gloss">house big</div>
  </div>
</div>

<h3><img src="img/check.svg" width="36px">
  Adjectives must correspond in gender & number with the noun.</h3>

<div class="flex">
  <div class="num">1A</div>
  <div class="content">
    <div class="arb">بيت كبير</div>
    <div class="eng">a big house</div>
    <div class="gloss"><span class="blu">M</span>.house <span class="blu">M</span>.big</div>
  </div>
</div>

<div class="flex">
  <div class="num">1B</div>
  <div class="content">
    <div class="arb">سَيّارة منيح<span class="mag">ة</span></div>
    <div class="eng">a good car</div>
    <div class="gloss"><span class="mag">F</span>.car good-<span class="mag">F</span></div>
  </div>
</div>

<div class="flex">
  <div class="num">1C</div>
  <div class="content">
    <div class="arb">بيوت كبير<span class="mag">ة</span></div>
    <div class="eng">big houses</div>
    <div class="gloss"><span class="yel">P</span>.houses big-<span class="mag">F</span></div>
  </div>
</div>

<p>Notice that plural agreement usually just means making the adjective feminine, since non-human plurals are all treated as feminine in Arabic (see <a href="">Grammatical Number</a>). However, if you were actually talking about people, it would be:</p>

<div class="flex">
  <div class="num">2A</div>
  <div class="content">
    <div class="arb">وَلَد منيح</div>
    <div class="eng">a good boy</div>
    <div class="gloss"><span class="blu">M</span>.boy <span class="blu">M</span>.good</div>
  </div>
</div>

<div class="flex">
  <div class="num">2B</div>
  <div class="content">
    <div class="arb">بِنت منيح<span class="mag">ة</span></div>
    <div class="eng">a good girl</div>
    <div class="gloss"><span class="mag">F</span>.girl good-<span class="mag">F</span></div>
  </div>
</div>

<div class="flex">
  <div class="num">2C</div>
  <div class="content">
    <div class="arb">ولاد مناح</div>
    <div class="eng">good kids</div>
    <div class="gloss"><span class="yel">P</span>.children <span class="yel">P</span>.good</div>
  </div>
</div>

<h3><img src="img/check.svg" width="36px">
  Adjectives must correspond in definiteness with the noun.</h3>

<p>If the noun is definite in any way (see: <a href="?page=02">the Definite Article</a>), then the adjective must be marked as definite by using the Definite Article:</p>

<div class="flex">
  <div class="num">3A</div>
  <div class="content">
    <div class="arb"><span class="grn">ال</span>بيت <span class="grn">ال</span>كبير</div>
    <div class="eng">the big house</div>
    <div class="gloss"><span class="grn">DEF</span>-house <span class="grn">DEF</span>-big</div>
  </div>
</div>

<div class="flex">
  <div class="num">3B</div>
  <div class="content">
    <div class="arb">بيت<span class="cyn">ي</span> <span class="grn">ال</span>كبير</div>
    <div class="eng">my big house</div>
    <div class="gloss">house-<span class="cyn">1S</span> <span class="grn">DEF</span>-big</div>
  </div>
</div>

<div class="flex">
  <div class="num">3C</div>
  <div class="content">
    <div class="arb"><u class="stress">بيت أحمد</u> <span class="grn">ال</span>كبير</div>
    <div class="eng">Ahmad's big house</div>
    <div class="gloss"><span class="grn">DEF</span>.[<span class="yel">IND</span>.house <span class="grn">DEF</span>.Ahmad] <span class="grn">DEF</span>-big</div>
  </div>
</div>

<p>Notice that <b>بيت أحمد</b> (<b>3C</b>) is a definite <b>إضافة</b> phrase which internally is made up of an indefinite noun followed by a definite noun. Even though the word <b>بيت</b> by itself would be indefinite (as in <b>1A</b>), since the entire phrase <b>بيت أحمد</b> is definite, the adjective modifying it must be definite as well. Additionally, notice that the adjective comes after the <b>إضافة</b> (as in <b>3C</b>). Likewise, "the Old City of Jerusalem" would be:</p>

<div class="flex">
  <div class="num">4A</div>
  <div class="content">
    <div class="arb"><u class="stress">بَلدة القُدس</u> <span class="grn">ال</span>قديمة</div>
    <div class="eng">the Old City of Jerusalem</div>
    <div class="gloss"><span class="grn">DEF</span>.[<span class="yel">IND</span>.city <span class="grn">DEF</span>.Jerusalem] <span class="grn">DEF</span>-old</div>
  </div>
</div>

<p>As you can see, the <b>إضافة</b> comes first, then the adjective. Although <b>بلدة</b> by itself is indefinite, the <b>إضافة</b> phrase <b>بلدة القدس</b> is definite, so the adjective is definite as well.</p>

<p>One last thing worth restating is that adjectives only correspond in definiteness with the noun when they are <b>attributive</b> (i.e. connected to the noun in a single noun phrase, as in "<u>the <b>big</b> house</u>"). Conversely, <b>predicative</b> adjectives (as in "<u>the house</u> is <b>big</b>") are always indefinite:

<div class="flex">
  <div class="num">4B</div>
  <div class="content">
    <div class="arb"><u class="stress">بَلدة القُدس</u> قديمة</div>
    <div class="eng">the city of Jerusalem is old</div>
    <div class="gloss"><span class="grn">DEF</span>.[<span class="yel">IND</span>.city <span class="grn">DEF</span>.Jerusalem] = <span class="yel">IND</span>.old</div>
  </div>
</div>

<p>That's why it's really important to use the article correctly: While <b>4B</b> is not wrong per se, it's a nominal sentence with a predicative adjective, not a noun phrase with an attributive adjective.</p>

<h1 id="elative">Elative Adjectives</h1><div class="line"></div>

<p>Now let's talk about the other type of adjective in Arabic: the Elative Adjective.</p>

<h2>Formation</h2>

<p>Regarding its form: You can easily tell that something is an elative adjective if it has the form <b>أفعَل</b>, as in:

<div class="flex">
  <div class="content">
    <div class="arb">أكبَر</div>
    <div class="eng">biggest</div>
  </div>
  <div class="content">
    <div class="arb">أصغَر</div>
    <div class="eng">smallest</div>
  </div>
  <div class="content" style="margin-right:0">
    <div class="arb">أكتَر</div>
    <div class="eng">most</div>
  </div>
</div>

<p>If the third letter of the root is repeated, it becomes geminated:</p>

<div class="flex">
  <div class="content">
    <div class="arb">أقَلّ</div>
    <div class="eng">least</div>
  </div>
  <div class="content">
    <div class="arb">أجَدّ</div>
    <div class="eng">newest</div>
  </div>
</div>

<p>If the last letter is missing altogether, you just leave it blank:</p>

<div class="flex">
  <div class="content">
    <div class="arb">أغلى</div>
    <div class="eng">costliest</div>
  </div>
  <div class="content">
    <div class="arb">أحلى</div>
    <div class="eng">nicest</div>
  </div>
</div>

<p>Aside from the root letters, notice that the original form of the adjective doesn't affect its elative form.</p>

<h2>Meaning</h2>

<p>An <b>elative</b> adjective is an adjective that conveys both a comparative meaning (like "bigger") or a superlative meaning (like "biggest") — but which meaning it conveys depends on how it's used.

<p>When the elative is used as a <b>predicative</b> adjective, it has a comparative meaning:

<div class="flex">
  <div class="num">6A</div>
  <div class="content">
    <div class="arb"><span class="grn">ال</span>بيت كبير</div>
    <div class="eng">the house is big</div>
    <div class="gloss">[<span class="grn">DEF</span>-house] = <span class="yel">IND</span>.big</div>
  </div>
</div>

<div class="flex">
  <div class="num">6B</div>
  <div class="content">
    <div class="arb"><span class="grn">ال</span>بيت أكبَر</div>
    <div class="eng">the house is bigger</div>
    <div class="gloss">[<span class="grn">DEF</span>-house] = <span class="yel">E</span>.big</div>
  </div>
</div>

<p>This use of the elative goes hand-in-hand with <b>من</b>, as in:</p>

<div class="flex">
  <div class="num">6B</div>
  <div class="content">
    <div class="arb"><span class="grn">ال</span>بيت أكبَر من هيك</div>
    <div class="eng">the house is bigger than that</div>
    <div class="gloss">[<span class="grn">DEF</span>-house] = <span class="yel">E</span>.big from so</div>
  </div>
</div>

<p>As you can see, the <b>predicative</b> use of elative adjectives is just like the use of regular adjectives in Arabic, and indeed just like the use of the comparative in English.</p>

<p>As for their use as <b>attributive</b> adjectives (i.e. connected to the noun in a single noun phrase), the story is a bit trickier. If the elative goes after the noun — again, like a regular adjective — the meaning is simply comparative:</p>

<div class="flex">
  <div class="num">7A</div>
  <div class="content">
    <div class="arb">بيت أكبَر</div>
    <div class="eng">a bigger house</div>
    <div class="gloss">[<span class="yel">IND</span>.house <span class="yel">E</span>.big]</div>
  </div>
</div>

<p>However, if the elative goes before the noun, the meaning is superlative:</p>

<div class="flex">
  <div class="num">8A</div>
  <div class="content">
    <div class="arb">أكبَر بيت</div>
    <div class="eng">the biggest house</div>
    <div class="gloss"><span class="grn">DEF</span>.[<span class="grn">E</span>.big <span class="yel">IND</span>.house]</div>
  </div>
</div>

<p>The most interesting thing about this minimal pair is actually not that the meaning is comparative in one & superlative in the other, but rather that it's indefinite in one & definite in the other, even though the adjective itself has not taken the article or changed in form in any way. How can that be?</p>

<p>That's because in Arabic, an elative adjective with a superlative meaning inherently refers to something definite. Since <b>أكبَر</b> (as a superlative) inherently refers to "the biggest" thing, adding an article would be redundant; <b>أكبَر بيت</b> — without any article — already means "the biggest house" (<b>7B</b>).</p>

<p>Note that it's only when the elative has a superlative meaning that it's placed before the noun & makes the entire noun phrase definite. As we saw in <b>6A</b> vs. <b>6B</b>, the comparative use of the elative works essentially like a regular adjective, meaning it should technically correspond in gender, number & definiteness with the noun.</p>

<p>In practice, though, there are some caveats — for the better. Compare <b>بيت كبير</b> (<b>1A</b>) & <b>بيت أكبَر</b> (<b>7A</b>). If you want to turn <b>1A</b> into a definite phrase, you just attach the article to the noun & to the adjective: <b>البيت الكبير</b> (<b>3A</b>). Technically, you can make <b>7A</b> definite in the same way:</p>

<div class="flex">
  <div class="num">9A</div>
  <div class="content">
    <div class="arb"><span class="grn">ال</span>بيت <span class="grn">ال</span>أكبَر</div>
    <div class="eng">the bigger house</div>
    <div class="gloss">[<span class="grn">DEF</span>-house <span class="grn">DEF</span>-<span class="yel">E</span>.big]</div>
  </div>
</div>

<p>But this type of phrasing is actually not very common. Why? Firstly, consider that elative adjectives are either used before the noun as inherently definite attributive superlatives (<b>7B</b>), or as attributive (<b>7A</b>) or predicative (<b>6B</b>) comparatives that function just like regular adjectives. But since attributive superlatives (<b>7B</b>) & predicative adjectives (<b>4B</b> & <b>6B</b>) never take the definite article, the only case where an elative could possibly take the article is in the case of a definite attributive comparative, like <b>البيت الأكبَر</b> (<b>7C</b>). However, for all practical purposes, (<b>7C</b>) is pragmatically equivalent to (<b>7B</b>), making the entire existence of this phrasing redundant.
</p>

<p>Secondly, this is compounded by the fact that elatives as superlatives never correspond in gender & number with the noun, hence:</p>

<div class="flex">
  <div class="num">8A</div>
  <div class="content">
    <div class="arb">أكبَر بيت</div>
    <div class="eng">the biggest house</div>
    <div class="gloss">[<span class="grn">E</span>.big <span class="blu">M</span>.house]</div>
  </div>
</div>

<div class="flex">
  <div class="num">8B</div>
  <div class="content">
    <div class="arb">أحسَن سَيّارة</div>
    <div class="eng">the best car</div>
    <div class="gloss">[<span class="grn">E</span>.good <span class="mag">F</span>.car]</div>
  </div>
</div>

<p>Although technically elatives as comparatives should function as regular adjectives, in the dialect they've actually lost their inflections already, hence:</p>

<div class="flex">
  <div class="num">1A</div>
  <div class="content">
    <div class="arb">بيت كبير</div>
    <div class="eng">a big house</div>
    <div class="gloss"><span class="blu">M</span>.house <span class="blu">M</span>.big</div>
  </div>

  <div class="num">1B</div>
  <div class="content">
    <div class="arb">سَيّارة منيح<span class="mag">ة</span></div>
    <div class="eng">a good car</div>
    <div class="gloss"><span class="mag">F</span>.car <span class="mag">F</span>.new</div>
  </div>
</div>

<div class="flex">
  <div class="num">7A</div>
  <div class="content">
    <div class="arb">بيت أكبَر</div>
    <div class="eng">a bigger house</div>
    <div class="gloss"><span class="blu">M</span>.house <span class="yel">E</span>.big</div>
  </div>

  <div class="num">7B</div>
  <div class="content">
    <div class="arb">سَيّارة أحسَن</div>
    <div class="eng">a better car</div>
    <div class="gloss"><span class="mag">F</span>.car <span class="yel">E</span>.new</div>
  </div>
</div>

<p>Hence, this makes the structure of <b>9A</b> — which inflects for definiteness but not for gender or number — seem even more strange, while the grammatically "correct" form (<b>9B</b>) is just awkwardly formal.</p>

<div class="flex">
  <div class="num">9B</div>
  <div class="content">
    <div class="arb"><span class="grn">ال</span>سَيّارة <span class="grn">ال</span>أحسَن</div>
    <div class="eng">the better car</div>
    <div class="gloss">? [<span class="grn">DEF</span>-<span class="mag">F</span>.car <span class="grn">DEF</span>-<span class="yel">E</span>.good]</div>
  </div>
</div>

<div class="flex">
  <div class="num">9C</div>
  <div class="content">
    <div class="arb"><span class="grn">ال</span>سَيّارة <span class="grn">ال</span>حُسنى</div>
    <div class="eng">the better car</div>
    <div class="gloss"># [<span class="grn">DEF</span>-<span class="mag">F</span>.car <span class="grn">DEF</span>-<span class="mag">F</span><span class="yel">E</span>.good]</div>
  </div>
</div>

<p>So there's a lot of pressure to entirely avoid the really marginal, superfluous & grammatically problematic case of the definite attributive comparative (<b>9A</b> & <b>9B</b>) in favor of the much "neater" superlative structure of <b>7D</b>. Linguistically speaking, this seems to be moving in the direction of a state of affairs in which the elative is a completely static form that never inflects for anything.</p>

<h1 id="ordinal">Ordinal Numbers</h1><div class="line"></div>

<p>I asked you why (the big bus) is IL-BUS LI-KBIR, but (the last bus) is AAXER BUS. Well, the structure of AAXER BUS should look familiar to you by now. One more thing to know is that all ordinal terms work grammatically like elatives, from <b>أوّل</b> (the first) to <b>آخر</b> (the last). Hence, while "the big bus" is <b>الباص الكبير</b>, "the final bus" is <b>آخر باص</b>. Just as in <b>7B</b>, no article is needed, because the superlative elative inherently heads a definite phrase. Similarly, just <b>باص أكبَر</b> is "a bigger bus", <b>باص آخر</b> is "a later bus".</p>

<p>Now compare the following phrases:</p>

<div class="flex">
  <div class="num">10A</div>
  <div class="content">
    <div class="arb">مَرّة تاني<span class="mag">ة</span></div>
    <div class="eng">a second time</div>
    <div class="gloss">[<span class="yel">IND</span>.time <span class="yel">IND</span>.second-<span class="mag">F</span>]</div>
  </div>
</div>

<div class="flex">
  <div class="num">10B</div>
  <div class="content">
    <div class="arb"><span class="grn">ال</span>مَرّة <span class="grn">ال</span>تاني<span class="mag">ة</span></div>
    <div class="eng">the second time</div>
    <div class="gloss">[<span class="grn">DEF</span>-time <span class="grn">DEF</span>-second-<span class="mag">F</span>]</div>
  </div>
</div>

<div class="flex">
  <div class="num">10C</div>
  <div class="content">
    <div class="arb">تاني مَرّة</div>
    <div class="eng">the second time</div>
    <div class="gloss"><span class="grn">DEF</span>.[<span class="grn">E</span>.second (<span class="yel">IND</span>.time)]</div>
  </div>
</div>

<p>Now you can see that <b>11A</b> is the same in meaning as <b>11B</b>, while <b>11B</b> & <b>11C</b> are structurally the same, except that <b>11C</b> is indefinite. Note that although ordinal terms can work gramatically like superlative elatives, they're otherwise regular adjectives, meaning they're not so resistant to the structure of <b>11B</b>; as you can see, they even inflect for gender, unlike <b>8B</b>. I would still say the structure of <b>11B</b> is comparatively more formal, especially in the case of <b>أوّل</b> and <b>آخر</b> specifically: even though more technically correct, <b>12B</b> comes off as extremely formal, so do use <b>12A</b>.</p>

<div class="flex">
  <div class="num">11A</div>
  <div class="content">
    <div class="arb">أوّل مَرّة</div>
    <div class="eng">the first time</div>
    <div class="gloss"><span class="grn">DEF</span>.[<span class="grn">E</span>.first (<span class="yel">IND</span>.time)]</div>
  </div>
</div>

<div class="flex">
  <div class="num">11B</div>
  <div class="content">
    <div class="arb"><span class="grn">ال</span>مَرّة <span class="grn">ال</span>أولى</div>
    <div class="eng">the first time</div>
    <div class="gloss"># [<span class="grn">DEF</span>-time <span class="grn">DEF</span>-<span class="mag">F</span><span class="yel">E</span>.second]</div>
  </div>
</div>

<h1>Complex Syntax</h1><div class="line"></div>

<p>Now that we understand the use of elative adjectives, some of the more unexpected syntax of Arabic starts to make more sense. Let's start by comparing two simple sentences. First:</p>

<div class="flex">
  <div class="num">12A</div>
  <div class="content">
    <div class="arb">شُفته مَرّة تاني<span class="mag">ة</span></div>
    <div class="eng">I saw him again</div>
    <div class="gloss">saw.1S:3M [<span class="yel">IND</span>.time <span class="yel">IND</span>.other-<span class="mag">F</span>]</div>
  </div>
</div>

<p>Note that <b>تاني</b> literally means "second", but by extension it means "another" — so <b>مَرّة تانية</b> is just like saying "another time" (i.e. "again"). In this sentence, <b>تاني</b> is structurally analogous to an attributive comparative (<b>7A</b>), so <b>مَرّة تانية</b> is a single indefinite noun phrase working as an adverb meaning "again". Now consider:</p>

<div class="flex">
  <div class="num">12B</div>
  <div class="content">
    <div class="arb">تاني مَرّة شُفته</div>
    <div class="eng">the next time I saw him</div>
    <div class="gloss"><span class="grn">DEF</span>.[<span class="grn">E</span>.other (<span class="yel">IND</span>.time <span class="yel">~</span> saw.1S:3M)]</div>
  </div>
</div>

<p>Although the words are almost exactly the same, <b>13B</b> is not even a complete sentence: the entire thing is actually just one definite noun phrase, in which the verb <b>شفته</b> is a relative clause that gives additional information about <b>مَرّة</b>. Notice that there's no definite article anywhere, because <b>تاني</b> is acting grammatically like a superlative elative here, making the entire phrase definite.</p>

<p>If you're very observant, you may be asking yourself why <a href="">the Relative Pronoun</a> <b>اللي</b> seems to be missing. As you may know, <b>اللي</b> initiates relative clauses, similar to "that" in English, as in:</p>

<div class="flex">
  <div class="num">13A</div>
  <div class="content">
    <div class="arb"><span class="grn">ال</span>زَلَمة <span class="grn">اِل</span>لي شُفته</div>
    <div class="eng">the guy I saw</div>
    <div class="gloss">[<span class="grn">DEF</span>-guy <span class="grn">~</span> saw.1S:3M]</div>
  </div>
</div>

<p>But while "that" is optional in English, in Arabic <b>اللي</b> is not optional: It must be used if the noun phrase is definite, but it must not be used if the noun phrase is indefinite. Hence:</p>

<div class="flex">
  <div class="num">13B</div>
  <div class="content">
    <div class="arb">زَلَمة شُفته</div>
    <div class="eng">a guy I saw</div>
    <div class="gloss">[<span class="yel">IND</span>.guy <span class="yel">~</span> saw.1S:3M]</div>
  </div>
</div>

<p>So what about <b>13B</b>? If the use of <b>تاني</b> makes it a definite phrase, why isn't <b>اللي</b> used? That's because <b>شفته</b> is not a relative clause of the definite noun <b>تاني مَرّة</b> — but rather a relative clause of the indefinite noun <b>مَرّة</b>. The word <b>تاني</b> is not emcompassing <b>مَرّة</b> directly, but rather it's encompassing the entire phrase <b>مَرّة شفته</b> & making it definite. Since <b>مَرّة شفته</b> by itself is indefinite, it would be ungrammatical to use <b>اللي</b> in this case. Conversely, it would be ungrammatical as well to insert a definite phrase (i.e. <b>المَرّة اللي شفته</b>) after <b>تاني</b>, as everything nested beneath the elative should be indefinite.</p>

<p>In sum, the elative adjective comes first, followed by an entirely indefinite phrase, with the outcome being a single definite unit. It's similar to <b>إضافة</b> in the sense that the first part of the structure must be indefinite, but the entire unite is made definite through the addition of the second definite element (see <b>3C</b>).</p>

<p>Also, remember than <b>تاني مَرّة شفته</b> is not a full sentence. A full sentence would be:</p>

<div class="flex">
  <div class="num">12B</div>
  <div class="content">
    <div class="arb">تاني مَرّة شُفته قُلتلّه لأ</div>
    <div class="eng">the next time I saw him I told him no</div>
    <div class="gloss"><span class="grn">DEF</span>.[<span class="grn">E</span>.other (<span class="yel">IND</span>.time <span class="yel">~</span> saw.1S:3M)] said.1S:3M no</div>
  </div>
</div>

<p>It may seem strange to have two verbs back-to-back like that, if you don't understand the function of each one. And this is not a marginal structure, by any means. It's very common to use elatives in general precisely on account of their many practical uses. Let's build the phrase: "the song i like to sing the most". Instead of starting with <b>الأغنية</b> — as the English suggests — the first element should be the elative: <b>أكتر</b> (the most). Next, everything nested beneath it must be a single indefinite unit: <b>أغنية بحبّ أغنّيها</b> (a song I like to sing); although this phrase is indefinite by itself, the elative makes everything nested beneath it into a single definite unit:</p>

<div class="flex">
  <div class="num">12B</div>
  <div class="content">
    <div class="arb">أكتَر أغنية بَحِبّ أغَنّيها</div>
    <div class="eng">the song I like to sing the most</div>
    <div class="gloss"><span class="grn">DEF</span>.[<span class="grn">E</span>.most (<span class="yel">IND</span>.song <span class="yel">~</span> 1S-like SBV.1S-sing:3F)]</div>
  </div>
</div>

<p>And that's everything you need to know about the <b>Elative Adjective</b> in Palestinian Arabic!</p>

