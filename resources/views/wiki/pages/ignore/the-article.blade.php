<h1 id="definite">the Definite Article</h1><div class="line"></div>

<p>In this lesson, we're going to discuss <b>the Definite Article</b> and <b>the Demonstratives</b> in Palestinian Arabic. In order to master the use of these two concepts, there are a few fundamental ideas we need to understand:</p>

<p>First, let's discuss the difference between <a href="https://en.wikipedia.org/wiki/Definiteness" target="_blank">definite</a> and <b>indefinite</b> nouns. As the name suggests, definite nouns are specific. In English, definite nouns are marked by the article <b>the</b>, while indefinite nouns are marked by the article <b>a(n)</b>.</p>

<div class="flex">
  <div class="num">1A</div>
  <div class="content">
    <div class="eng">the house</div>
    <div class="gloss"><span class="grn">DEF</span>-house</div>
  </div>
  <div class="num">1B</div>
  <div class="content">
    <div class="eng">a house</div>
    <div class="gloss"><span class="yel">IND</span>.house</div>
  </div>
</div>

<p>Articles are not the only way definiteness is marked. Proper nouns, like <b>Palestine</b>, are inherently definite. Likewise, all possessed nouns — like <b>my country</b> — are definite as well. While this may seem relatively basic, the reason we need to understand this on a technical level is that not all languages mark definiteness and indefiniteness in the same way. Unlike English, Arabic only explicitly marks <b>definiteness</b> through the article <b>الـ (il-)</b>, while the noun by itself is automatically indefinite:</p>

<div class="flex">
  <div class="num">1A</div>
  <div class="content" onclick="bēt$ilbet.play()">
    <div class="arb"><span class="grn">ال</span>بيت</div>
    <div class="eng">the house</div>
    <div class="gloss"><span class="grn">DEF</span>-house</div>
  </div>
  <div class="num">1B</div>
  <div class="content" onclick="bēt.play()">
    <div class="arb">بيت</div>
    <div class="eng">a house</div>
    <div class="gloss"><span class="yel">IND</span>.house</div>
  </div>
</div>

<p>Again, notice that <b>بيت (bēt "house")</b> by itself is automatically indefinite. This seemingly minor point is very significant: The number one reason Arabic learners don't use the <b>Definite Article</b> correctly is that they simply equate it to English <b>the</b>. In reality, though, the way Arabic handles definiteness and indefiniteness is very different from that of English, and it must be understood in its own right.</p>

<h1>Definiteness Agreement</h1><div class="line"></div>

<p>Most languages require some form of <a href="https://en.wikipedia.org/wiki/Agreement_(linguistics)" target="_blank">grammatical agreement</a> between nouns and adjectives. In a language with <b>number agreement</b>, for instance, if a noun is grammatically plural then the adjective that modifies it should be in its plural form as well. Arabic has three types of <b>grammatical agreement</b>: <b>number agreement</b>, <b>gender agreement</b> and <b>definiteness agreement</b>.</p>

<p>In Arabic, adjectives always come after the noun. In order to indicate that the terms are grammatically connected as sister elements of a single phrase, adjectives must agree in definiteness with the noun.</p>

<div class="flex">
  <div class="num">2A</div>
  <div class="content" onclick="bēt$ilbet_likbir.play()">
    <div class="arb"><span class="grn">ال</span>بيت <span class="grn">ال</span>كبير</div>
    <div class="eng">the big house</div>
    <div class="gloss"><span class="grn">DEF</span>-house <span class="grn">DEF</span>-big</div>
  </div>

  <div class="num">2B</div>
  <div class="content" onclick="bēt$bēt_kbir.play()">
    <div class="arb">بيت كبير</div>
    <div class="eng">a big house</div>
    <div class="gloss"><span class="yel">IND</span>.house <span class="yel">IND</span>.big</div>
  </div>
</div>

<p>That may look strange if you translate it literally, but you have to avoid overanalyzing each word in isolation, and think of the phrase as a single definite unit. On the other hand, if the entire phrase is supposed to be indefinite (<b>2B</b>), no articles are used at all.</p>

<p>Just remember that the Definite Article is not the only way that definiteness is marked, and you can't mark definiteness twice:</p>

