    <div class="eng">Ramallah is a small city</div>
    <div class="gloss"><span class="grn">DEF</span>.Ramallah = <span class="yel">IND</span>.city <span class="yel">IND</span>.small</div>
  </div>
</div>

<div class="flex">
  <div class="num">2A</div>
  <div class="content">
    <div class="arb">مَدينة <span class="grn">رام الله</span> زغيرة</div>
    <div class="eng">the city of Ramallah is small</div>
    <div class="gloss"><span class="grn">DEF</span>.[<span class="yel">IND</span>.city <span class="grn">DEF</span>.Ramallah] = <span class="yel">IND</span>.small</div>
  </div>
</div>

<p>Even though the first noun in an <b>Iḍāfa</b> is not individually marked as definite, the entire phrase is definite as a whole. Marking the head noun as definite is an ungrammatical double-marking of definiteness. Consequently, the phrase wouldn't be interpreted as <b>Iḍāfa</b> to begin with: it would be interpreted as two nouns in a <b>nominal sentence</b> which — although grammatical — does not carry the intended meaning.</p>

