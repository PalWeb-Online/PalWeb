<h1>Demonstrative Determiners</h1><div class="line"></div>

<p>In <a href="?page=02">Lesson 2</a>, we learned about the <b>Demonstrative Pronouns</b> in the dialect. One very important thing to note is that the <b>Demonstratives</b> don't function only as <b>Pronouns</b>; they function as <b>Determiners</b> as well. While <b>Demonstrative Pronouns</b> are standalone noun phrases, <b>Demonstrative Determiners</b> are part of a broader noun phrase.</p>

<div class="flex">
  <div class="num">8A</div>
  <div class="content">
    <div class="eng">this is a book</div>
    <div class="gloss">this = [book]</div>
  </div>
  <div class="num">8B</div>
  <div class="content">
    <div class="eng">this book is new</div>
    <div class="gloss">[this-book] = new</div>
  </div>
</div>

<p>In Arabic, what distinguishes the use of <b>Demonstratives</b> as <b>Pronouns</b> vs. <b>Determiners</b> is that, when they are <b>Pronouns</b>, they are definite nouns followed by indefinite phrases, whereas they are components of a broader definite phrase when used as <b>Determiners</b>. In practical terms, the <b>Demonstrative</b> is followed by an indefinite noun in the first case, whereas it is followed by a definite noun in the second. In fact, the <b>Demonstrative Determiners</b> cannot be attached to indefinite nouns at all; they can only be attached to definite nouns marked by the <b>Definite Article</b>.</p>

<div class="flex">
  <div class="num">8A</div>
  <div class="content" onclick="kitāb$hāda_ktāb.play()">
    <div class="title">PRONOUN</div>
    <div class="arb">هَدا كتاب</div>
    <div class="eng">this is a book</div>
    <div class="gloss">[DEF.this] = IND.book</div>
  </div>
  <div class="num">8B</div>
  <div class="content" onclick="kitāb$hāda_liktāb.play()">
    <div class="title">DETERMINER</div>
    <div class="arb">هَدا <span class="grn">ال</span>كتاب</div>
    <div class="eng">this book</div>
    <div class="gloss">[this <span class="grn">DEF</span>-book]</div>
  </div>
</div>

<p>In theory, the <b>Demonstrative</b> in <b>8B</b> could be interpreted as a <b>Pronoun</b> followed by a definite noun (i.e. <b>"this is the book"</b>). However, in practice, the <b>Demonstratives</b> as <b>Determiners</b> are usually shortened and attached to the article. In contrast, the full <b>هدا</b> is somewhat emphatic.</p>

