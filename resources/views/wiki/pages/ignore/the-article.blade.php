<h1 id="idafa">Non-Agreement Structures</h1><div class="line"></div>

<p>Observing definiteness agreement is really important in Arabic, because other grammatical structures are built specifically around this feature. Recall, for instance, that the verb <b>كان (kān "to be")</b> isn't used in the <b>Present Tense</b>. When a <b>definite</b> noun is directly followed by an <b>indefinite</b> phrase, an equivalence is formed between the two.</p>

<div class="flex">
  <div class="num">2A</div>
  <div class="content" onclick="bēt$ilbet_likbir.play()">
    <div class="arb"><span class="grn">ال</span>بيت <span class="grn">ال</span>كبير</div>
    <div class="eng">the big house</div>
    <div class="gloss"><span class="grn">DEF</span>-house <span class="grn">DEF</span>-big</div>
  </div>
</div>

<div class="flex">
  <div class="num">3</div>
  <div class="content" onclick="bēt$ilbet_kbir.play()">
    <div class="arb"><span class="grn">ال</span>بيت كبير</div>
    <div class="eng">the house is big</div>
    <div class="gloss">[<span class="grn">DEF</span>-house] = <span class="yel">IND</span>.big</div>
  </div>
</div>

<div class="box">
  <h1>TIP</h1><div class="line"></div>
  <p>Grammatically, there's no specific way to say that "a house is nice", because the nominal phrasing works specifically via this definite-indefinite pairing; however, even though "a house is nice" is grammatically correct in English, it doesn't mean anything useful anyway. If you want more information about this specific topic, check out my video about the correct use of <b>كان (kān "to be")</b> and <b>صار (ṣār "to become")</b>.</p>
</div>

<p>Finally, if an <b>indefinite</b> noun is followed by a <b>definite</b> noun, the two are linked into a single definite phrase through a construction known as <b>iḍāfa (إضافة)</b>. Compare the following two definite phrases:</p>

<div class="flex">
  <div class="num">4A</div>
  <div class="content" onclick="lahje$illahje_ilfalasṭīniyye.play()">
    <div class="arb"><span class="grn">ال</span>لَهجة <span class="grn">ال</span>فَلَسطينيّة</div>
    <div class="eng">the Palestinian dialect</div>
    <div class="gloss"><span class="grn">DEF</span>-dialect <span class="grn">DEF</span>-Palestinian</div>
  </div>
</div>

<div class="flex">
  <div class="num">4B</div>
  <div class="content" onclick="lahje$lahjet_falasṭīn.play()">
    <div class="arb">لَهجة <span class="grn">فَلَسطين</span></div>
    <div class="eng">the dialect of Palestine</div>
    <div class="gloss"><span class="grn">DEF</span>.[<span class="yel">IND</span>.dialect <span class="grn">DEF</span>.Palestine]</div>
  </div>
</div>

<p>In <b>4A</b>, an adjective (<b>"Palestinian"</b>) is chained to a noun (<b>"dialect"</b>) by marking both with the <b>Definite Article</b>. As we see in <b>4B</b>, two nouns are linked together through <b>Iḍāfa</b>. In English, we chain together nouns with the word <b>"of"</b> (e.g. <b>"the dialect of the region"</b>). In Arabic, nouns are linked together by making the head noun indefinite and the qualifying noun as definite. Don't forget that the <b>Definite Article</b> is not the only way of marking definiteness (<b>"Palestine"</b> is already definite because it's a proper noun). Notice that <b>4B</b> is completely definite, even though it has no articles at all.</p>

