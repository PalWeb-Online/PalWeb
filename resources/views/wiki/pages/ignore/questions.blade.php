<h1>Asking Questions</h1><div class="line"></div>

<p>In this lesson, we’re going to discuss <b>Asking Questions</b> in Palestinian Arabic.</p>

<p>Asking Questions is actually quite easy in the dialect. Firstly, <b>Yes-No Questions</b> are simply asked by raising one’s tone of voice, exactly as is done in English.</p>

<div class="flex">
  <div class="content">
    <div class="arb">فيه مَيّ سُخنة بالبيت</div>
    <div class="eng">there is hot water in the house</div>
  </div>
</div>

<div class="flex">
  <div class="content">
    <div class="arb">فيه مَيّ سُخنة بالبيت؟</div>
    <div class="eng">is there hot water in the house?</div>
  </div>
</div>

<p>What we should really focus on are the <b>Question Words</b> used in the dialect. Many of them are similar — but not exactly the same — as the ones used in Standard Arabic.</p>

<h1>What & Why</h1><div class="line"></div>

<div class="flex">
  <div class="content">
    <div class="arb">شو؟</div>
    <div class="eng">what?</div>
  </div>
</div>

<div class="flex">
  <div class="content">
    <div class="arb">أيش؟</div>
    <div class="eng">what?</div>
  </div>
</div>

<p>As you can see, these two have the exact same meaning and they are completely interchangeable.</p>

<div class="flex">
  <div class="content">
    <div class="arb">شو بِدّك نسَوّي بُكرا؟</div>
    <div class="eng">what do you want us to do tomorrow?</div>
  </div>
</div>

<div class="flex">
  <div class="content">
    <div class="arb">أيش بِدّك نسَوّي بُكرا؟</div>
    <div class="eng">what do you want us to do tomorrow?</div>
  </div>
</div>

<div class="app-tip">
  <p>FUN FACT — Originally, <b>أيش</b> is a contraction of <b>أي شي (ʔay ši “which thing”)</b>, while <b>شو</b> is a contraction of <b>أي شي هو (ʔay ši hū “which thing is it”)</b>. Because of that, I have seen some say that <b>شو</b> is in the nominative case (e.g. it would be used for the subject of a sentence), while <b>أيش</b> is in the accusative case (e.g. it would be used for the object of a sentence). Whether or not this was the case in an earlier stage of the dialect, this does not bear examination in practice today; they are actually interchangeable.</p>
</div>

<p>If we combine <b>أيش</b> with the preposition <b>لـ (l- “for”)</b>, we get:</p>

<div class="flex">
  <div class="content">
    <div class="arb">ليش؟</div>
    <div class="eng">why?</div>
    <div class="gloss">for what?</div>
  </div>
</div>

<div class="flex">
  <div class="content">
    <div class="arb">ليش ما بِدّك توكِل؟</div>
    <div class="eng">why don't you want to eat?</div>
  </div>
</div>

<p>We’ll revisit this word in <b>Lesson 16 (Cause & Effect)</b>.</p>

<h1>How Much</h1><div class="line"></div>

<p>While we’re on the topic of <b>أيش</b> — we can combine it with <b>قَدّ (ʔadd “as much as”)</b> to get:</p>

<div class="flex">
  <div class="content">
    <div class="arb">قَدّيش؟</div>
    <div class="eng">how much?</div>
    <div class="gloss">as much as what?</div>
  </div>
</div>

<p>This term is more versatile that you might think at first. It’s used to refer not just to the quantity of something but to its degree, and it’s used for telling time as well.</p>

<div class="flex">
  <div class="content">
    <div class="arb">قَديش الساعة؟</div>
    <div class="eng">what time is it?</div>
    <div class="gloss">how much is the hour?</div>
  </div>
</div>

<div class="flex">
  <div class="content">
    <div class="arb">بقَدّيش البلوزة؟</div>
    <div class="eng">how much is the shirt?</div>
    <div class="gloss">by how much is the shirt?</div>
  </div>
</div>

<div class="flex">
  <div class="content">
    <div class="arb">مِش طَبيعي قَدّيش هو نِكد</div>
    <div class="eng">it's crazy how pesky he is</div>
    <div class="gloss">it's not natural how much he is pesky</div>
  </div>
</div>

<div class="app-tip">
<p>TIP — Some people say <b>أيه (ʔēh)</b> instead of <b>أيش (ʔēš)</b>, affecting other terms built from <b>أيش (ʔēš)</b>:</p>

<div class="flex">
  <div class="content">
    <div class="arb">أيه؟</div>
    <div class="eng">what?</div>
  </div>
</div>

<div class="flex">
  <div class="content">
    <div class="arb">ليه؟</div>
    <div class="eng">why?</div>
  </div>
</div>

<div class="flex">
  <div class="content">
    <div class="arb">قَدّيه؟</div>
    <div class="eng">how much?</div>
  </div>
</div>
</div>

<h1>Where & How</h1><div class="line"></div>

<p>We’ve already covered <b>وين (wēn “where?”)</b> and <b>كيف (kīf “how?”)</b> in <b>Lesson 3 (Enclitic Determiners)</b>, when we discussed how we can identify their point of reference by attaching <b>enclitic determiners</b> to them; these are the only two Question Words we can do this to.</p>

<div class="flex flashcard">
  <div class="content">
    <div class="front">
      <div class="arb">وين؟</div>
      <div class="eng">where?</div>
    </div>
    <div class="back">
      <div class="arb">وينك؟</div>
      <div class="eng">where are you?</div>
    </div>
  </div>

  <div class="content">
    <div class="front">
      <div class="arb">كيف؟</div>
      <div class="eng">how?</div>
    </div>
    <div class="back">
      <div class="arb">كيفك؟</div>
      <div class="eng">how are you?</div>
    </div>
  </div>
</div>

<p>Try not to get confused, because <b>وين</b> happens to sound quite similar to the English word “when” — but it’s not. In Egypt and possibly in some areas of Palestine, <b>فين (fēn “where?”)</b> is used instead.</p>

<div class="flex">
  <div class="content">
    <div class="arb">عَلى وين راح الزَلَمة؟</div>
    <div class="eng">where did the man go?</div>
  </div>
</div>

<div class="flex">
  <div class="content">
    <div class="arb">مِن وين إنتا؟</div>
    <div class="eng">where are you from?</div>
  </div>
</div>

<div class="flex">
  <div class="content">
    <div class="arb">كيف الولاد؟</div>
    <div class="eng">how are the children?</div>
  </div>
</div>

<div class="flex">
  <div class="content">
    <div class="arb">كيف بِدّك ترَوِّح؟</div>
    <div class="eng">how are you going to get back home?</div>
  </div>
</div>

<h1>Who & When</h1><div class="line"></div>

<p>We have two main different options for asking <b>“when”</b>:</p>

<div class="flex">
  <div class="content">
    <div class="arb">أيمتى؟</div>
    <div class="eng">when?</div>
  </div>
  <div class="content">
    <div class="arb">وينتى؟</div>
    <div class="eng">when?</div>
  </div>
</div>

<p>I recommend sticking to the more common <b>أيمتى</b> — directly related to Standard Arabic <b>متى (mata “when”)</b>; conversely, <b>وينتى</b> seems to be used regionally and it’s misleadingly similar to <b>وين (wēn “where?”)</b>. Another regional — particularly rural — alternative is <b>وقتيش (waktēš)</b>.</p>

<div class="flex">
  <div class="content">
    <div class="arb">أيمتى بِتحِبّ نِنزَل عالبَحر؟</div>
    <div class="eng">when would you like for us to go down to the sea?</div>
  </div>
</div>

<div class="flex">
  <div class="content">
    <div class="arb">لأيمتى رَح تكون مَشغول؟</div>
    <div class="eng">until when will you be busy?</div>
  </div>
</div>

<p>Lastly, not to be confused with <b>مِن (min “from”)</b> or Standard Arabic <b>مَن (man “who?”)</b>, <b>“who”</b> in the dialect is:</p>

<div class="flex">
  <div class="content">
    <div class="arb">مين؟</div>
    <div class="eng">who?</div>
  </div>
</div>

<div class="flex">
  <div class="content">
    <div class="arb">مين كان عِندك مبارِح؟</div>
    <div class="eng">who was at your place yesterday?</div>
  </div>
</div>

<div class="flex">
  <div class="content">
    <div class="arb">لَمين الشَنطة هاي؟</div>
    <div class="eng">whose bag is this?</div>
    <div class="gloss">for who is the bag this?</div>
  </div>
</div>

<p>As you can see, we can combine these Question Words with prepositions to ask pretty much anything you can think of!</p>

<p>And that’s everything you need to know about <b>Asking Questions</b> in Palestinian Arabic!</p>

