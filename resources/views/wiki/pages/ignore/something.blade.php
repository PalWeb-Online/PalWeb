<p>In Arabic, these terms are used more sparingly than in English. Pay attention to the situations in which we use these terms in English, but not in Arabic.</p>

<h2>Negation</h2>

<p>In English, double-negatives are not allowed. Hence, if we want to use the <b>"nothing"</b> terms in a negative sentence, we actually have to flip them into <b>"anything"</b> terms. In Arabic, double-negatives are neither forbidden nor required. If a sentence is already negative, we can simply use the <b>"something"</b> terms with the negative meaning implied; using the <b>"nothing"</b> terms is simply more emphatic. As for the <b>"anything"</b> terms: they’re never used in this situation, unlike how they’re used in English.</p>

MA FISH WALA ISHY BIL BET?

MA FISH ISHY BIL BET

<h2>Questions</h2>

<p>In addition to alternating with the <b>"nothing"</b> terms in negative sentences, the <b>"anything"</b> terms may be used synonymously with the <b>"something"</b> terms in questions. As for Arabic, once again the <b>"something"</b> terms are used by default; although using the <b>"anything"</b> terms is at least allowed in this case, it’s only used for extra emphasis.</p>

<div class="flex">
  <div class="num">2A</div>
  <div class="content">
    <div class="arb">بِدّك <span class="yel">إشي</span> من الدُكّان؟</div>
    <div class="eng">do you want anything from the store?</div>
    <div class="gloss">do you want <span class="yel">something</span> from the store?</div>
  </div>
</div>

<h2>Responses</h2>

<p>In some cases, the <b>"anything"</b> terms in English are interchangeable with <b>"whatever"</b> terms as well, especially when answering questions like the above. In English, we can answer simply by saying <b>"anything"</b> (<b>"whatever"</b> without a relative clause may sound rude). In Arabic, using the <b>"whatever"</b> construction is more natural, ironically because in Arabic it’s the use of the <b>"anything"</b> terms without a relative clause that seems comparatively incomplete.</p>

