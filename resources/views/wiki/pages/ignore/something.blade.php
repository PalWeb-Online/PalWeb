<h1>Something</h1><div class="line"></div>

<p>First, let's discuss the category of <b>something</b>. In a way, you already know how to form these types of words. In Arabic, the definite form of nouns — that is, with <a href="?page=02#definite" target="_blank">the Definite Article</a> — refers both to a specific instance of the noun (e.g. <b>الأكل "the food"</b>) and to the concept of the noun in general (e.g. <b>الأكل "food"</b>). Conversely, the indefinite form of nouns refers to a non-specific instance of the noun, and therefore inherently conveys the meaning of <b>"some"</b> in English (e.g. <b>أكل "[some] food"</b>).</p>

<p>Having said that, the word for <b>"something"</b> in Arabic is simply <b>إشي (ʔiši "[some] thing")</b>, since the indefinite form of the noun already conveys the meaning of <b>"some"</b>.</p>

<div class="flex">
  <div class="num">1</div>
  <div class="content">
    <div class="arb">بِدّي أطبُخ <span class="yel">إشي</span></div>
    <div class="eng">I want to cook something</div>
    <div class="gloss">I want to cook <span class="yel">IND</span>-thing</div>
  </div>
</div>

<p>What about <b>"someone"</b>? Again, if we remove the <b>"some"</b> we are left with <b>"one"</b>. Likewise, in Arabic, <b>"someone"</b> is simply <b>واحَد (wāḥad "[some] one")</b> — optionally shortened to <b>حَد (ḥad)</b> or <b>حَدا (ḥada)</b>.</p>

<div class="flex">
  <div class="num">2</div>
  <div class="content">
    <div class="arb">فيه <span class="yel">واحَد</span> بالحَمّام</div>
    <div class="eng">there's someone in the bathroom</div>
    <div class="gloss">there's <span class="yel">IND</span>-one in the bathroom</div>
  </div>
</div>

<p>I think now you're starting to see that having any word without the article in Arabic automatically implies the <b>"some"</b> before it. So, the same way we formed <b>إشي</b> (<b>"[some] thing"</b>) and <b>واحَد</b> (<b>"[some] one"</b>), the word <b>"somewhere"</b> is simply <b>مَكان (makān "[some] place")</b>!</p>

<div class="flex">
  <div class="num">3</div>
  <div class="content">
    <div class="arb">يَلا نروح عَلى <span class="yel">مَكان</span> حِلو اليوم</div>
    <div class="eng">let's go somewhere nice today</div>
    <div class="gloss">let's go <span class="yel">IND</span>-place nice today</div>
  </div>
</div>

<p>Now, to say <b>"sometime"</b>, we're going to use the word <b>"time"</b> by itself. But there are several terms for <b>"time"</b> in Arabic that have different uses; the word we use in this case is <b>مَرّة (marra "instance")</b>. Hence, <b>مَرّة</b> by itself simply means <b>"once"</b>, while <b>مَرّتين</b> means <b>"twice"</b> and <b>تَلات مَرّات</b> is <b>"three times"</b>. Notice that if the noun is countable, the singular form and the dual form already convey the meaning of <b>"one"</b> and <b>"two"</b> of that noun as well; it's not necessary to explicitly count them.</p>

<div class="flex">
  <div class="num">4A</div>
  <div class="content">
    <div class="arb">فيه أكتَر مِن <span class="yel">سَبَب</span></div>
    <div class="eng">there's more than one reason</div>
    <div class="gloss">there's more than <span class="yel">SING</span>.reason</div>
  </div>
</div>

<div class="flex">
  <div class="num">4B</div>
  <div class="content">
    <div class="arb">هو حَكالي <span class="yel">مَرّة</span> إنّه بِدّه ييجي عَلى لَندَن</div>
    <div class="eng">he told me once that he wants to come London</div>
    <div class="gloss">he told me <span class="yel">SING</span>.time that he wants to come London</div>
  </div>
</div>

<p>Of course, in the appropriate context, the word <b>مَرّة</b> can mean <b>"sometime"</b> as well.</p>

<div class="flex">
  <div class="num">5A</div>
  <div class="content">
    <div class="arb">خَلّينا نروح نِحضَر فيلم <span class="yel">مَرّة</span></div>
    <div class="eng">let's go watch a movie sometime</div>
    <div class="gloss">let's go watch a movie <span class="yel">IND</span>-<span class="yel">SING</span>.time</div>
  </div>
</div>

<p>In fact, the word <b>"sometimes"</b> in the dialect is simply the plural form <b>مَرّات</b> (<b>"[some] times"</b>):</p>

<div class="flex">
  <div class="num">5B</div>
  <div class="content">
    <div class="arb"><span class="yel">مَرّات</span> مَش فاهم شو عم بتِحكي</div>
    <div class="eng">sometimes I don't understand what you're saying</div>
    <div class="gloss"><span class="yel">IND</span>-<span class="yel">PLUR</span>.time I don't understand what you're saying</div>
  </div>
</div>

<h1>Everything</h1><div class="line"></div>

<p>Now that you know that indefinite nouns already convey the meaning of <b>"some"</b>, we're going to create terms like <b>"everything"</b> and <b>"nothing"</b> by attaching a word before the noun. Starting with all-inclusive terms like <b>"everything"</b>, the word we're going to attach is <b>كلّ (kull, "every")</b>.</p>

<div class="flex flashcard">
  <div class="content">
    <div class="front">
      <div class="arb">إشي</div>
      <div class="eng">something</div>
    </div>
    <div class="back">
      <div class="arb"><span class="blu">كُلّ</span> إشي</div>
      <div class="eng"><span class="blu">every</span>thing</div>
    </div>
  </div>
</div>

<div class="flex flashcard">
  <div class="content">
    <div class="front">
      <div class="arb">حَدا</div>
      <div class="eng">someone</div>
    </div>
    <div class="back">
      <div class="arb"><span class="blu">كُلّ</span> حَدا</div>
      <div class="eng"><span class="blu">every</span>one</div>
    </div>
  </div>
</div>

<div class="flex flashcard">
  <div class="content">
    <div class="front">
      <div class="arb">مَكان</div>
      <div class="eng">somewhere</div>
    </div>
    <div class="back">
      <div class="arb"><span class="blu">كُلّ</span> مَكان</div>
      <div class="eng"><span class="blu">every</span>where</div>
    </div>
  </div>
</div>

<div class="flex flashcard">
  <div class="content">
    <div class="front">
      <div class="arb">مَرّة</div>
      <div class="eng">one time</div>
    </div>
    <div class="back">
      <div class="arb"><span class="blu">كُلّ</span> مَرّة</div>
      <div class="eng"><span class="blu">every</span> time</div>
    </div>
  </div>
</div>

<p>Note that <b>كلّ إشي</b> is often contracted to <b>كلشي (kulši)</b>. Also, while <b>كلّ حَدا</b> refers to every individual person, the term <b>الكلّ</b> may be used to refer to everyone as a collective. Likewise, <b>كلّ مَرّة</b> specifically means <b>"every time"</b> — not <b>"always"</b>, which is <b>دايما</b>.</p>

<p>Before a singular noun, <b>كلّ</b> is an adjective meaning <b>"every [single thing]"</b>. But <b>كلّ</b> may be used as a noun as well; in this case, it refers to <b>"the entirety [of something]"</b>. Since <b>كلّ</b> would now be a noun in <a href="?page=02#idafa" target="_blank">Iḍāfa</a>, the noun that follows it must be definite:</p>

<div class="flex">
  <div class="content">
    <div class="arb"><span class="blu">كُلّ</span> بيت</div>
    <div class="eng"><span class="blu">every</span> house</div>
    <div class="gloss"><span class="blu">every</span> <span class="yel">IND</span>-house</div>
  </div>
  <div class="content">
    <div class="arb"><span class="blu">كُلّ</span> البيت</div>
    <div class="eng">the <span class="blu">entire</span> house</div>
    <div class="gloss">[<span class="yel">IND</span>-<span class="blu">entire</span> <span class="grn">DEF</span>-house]-<span class="grn">DEF</span></div>
  </div>
</div>

<div class="flex">
  <div class="content">
    <div class="arb">اشتَغَلت <span class="blu">كُلّ</span> اليوم</div>
    <div class="eng">I worked the <span class="blu">entire</span> day</div>
    <div class="gloss">I worked [<span class="yel">IND</span>-<span class="blu">entire</span> <span class="grn">DEF</span>-day]-<span class="grn">DEF</span></div>
  </div>
</div>

<p>When <b>كلّ</b> is a noun, you can attach a suffix to it too:</p>

<div class="flex">
  <div class="content">
    <div class="arb">كُلّه تَمام</div>
    <div class="eng">it's all good</div>
  </div>
</div>

<h1>Nothing</h1><div class="line"></div>

<p>As for negative terms like <b>"nothing"</b>, we're going to create them in the same way: by adding the term <b>ولا (wala)</b>, meaning <b>"not [a single thing]"</b>.</p>

