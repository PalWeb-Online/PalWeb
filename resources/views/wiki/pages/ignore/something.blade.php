<p>In this lesson, we're going to discuss how to refer to <b>something</b>, <b>anything</b>, <b>everything</b> and <b>nothing</b> in Palestinian Arabic. We can apply the same terms we use to form these words to pretty much any noun, so we're also going to form terms like <b>someone</b>, <b>everyone</b>, <b>somewhere</b>, <b>everywhere</b>, and so on.</p>

