<h2>Affirmative Clauses</h2>

<p>Having said all this, the main occasion to use the <b>"anything"</b> terms in Arabic is to create neither negative sentences nor to ask or answer questions, but simply in affirmative sentences, especially when followed by a relative clause (e.g. "anyone who X"; "anything that Y"). Notice that in these cases it’s not possible to exchange the <b>"anything"</b> terms with anything else without changing the meaning or being nonsensical in English.</p>

<div class="flex">
  <div class="num no material-icons-round">close</div>
  <div class="content">
    <div class="eng">someone who travels to China needs a visa</div>
  </div>
</div>

