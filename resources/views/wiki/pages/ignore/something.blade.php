    <div class="back">
      <div class="arb"><span class="grn">أي</span> إشي</div>
      <div class="eng"><span class="grn">any</span>thing</div>
    </div>
  </div>
</div>

<div class="flex flashcard">
  <div class="content">
    <div class="front">
      <div class="arb">حَدا</div>
      <div class="eng">someone</div>
    </div>
    <div class="back">
